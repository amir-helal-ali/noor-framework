/// Scaffold builder for fluent configuration
pub struct ScaffoldBuilder {
    config: ScaffoldConfig,
}

impl ScaffoldBuilder {
    pub fn new(model_name: &str) -> Self {
        Self {
            config: ScaffoldConfig {
                model_name: model_name.to_string(),
                table_name: format!("{}s", model_name.to_lowercase()),
                ..Default::default()
            },
        }
    }
    
    pub fn table(mut self, table: &str) -> Self {
        self.config.table_name = table.to_string();
        self
    }
    
    pub fn field(mut self, field: ScaffoldField) -> Self {
        self.config.fields.push(field);
        self
    }
    
    pub fn per_page(mut self, per_page: u32) -> Self {
        self.config.per_page = per_page;
        self
    }
    
    pub fn searchable(mut self, searchable: bool) -> Self {
        self.config.searchable = searchable;
        self
    }
    
    pub fn sortable(mut self, sortable: bool) -> Self {
        self.config.sortable = sortable;
        self
    }
    
    pub fn build(self) -> AdminGenerator {
        AdminGenerator::new(self.config)
    }
}

/// Field builder helper
pub struct FieldBuilder {
    field: ScaffoldField,
}

impl FieldBuilder {
    pub fn new(name: &str, field_type: FieldType) -> Self {
        Self {
            field: ScaffoldField {
                name: name.to_string(),
                field_type,
                label: name.to_string(),
                required: false,
                searchable: false,
                sortable: false,
                in_list: true,
                in_form: true,
                in_show: true,
                validation: vec![],
                default_value: None,
                options: None,
            },
        }
    }
    
    pub fn label(mut self, label: &str) -> Self {
        self.field.label = label.to_string();
        self
    }
    
    pub fn required(mut self) -> Self {
        self.field.required = true;
        self.field.validation.push(ValidationRule {
            rule: "required".to_string(),
            message: format!("{} is required", self.field.label),
        });
        self
    }
    
    pub fn searchable(mut self) -> Self {
        self.field.searchable = true;
        self
    }
    
    pub fn sortable(mut self) -> Self {
        self.field.sortable = true;
        self
    }
    
    pub fn in_list(mut self, show: bool) -> Self {
        self.field.in_list = show;
        self
    }
    
    pub fn in_form(mut self, show: bool) -> Self {
        self.field.in_form = show;
        self
    }
    
    pub fn validation(mut self, rule: &str, message: &str) -> Self {
        self.field.validation.push(ValidationRule {
            rule: rule.to_string(),
            message: message.to_string(),
        });
        self
    }
    
    pub fn options(mut self, opts: Vec<(String, String)>) -> Self {
        self.field.options = Some(opts);
        self
    }
    
    pub fn build(self) -> ScaffoldField {
        self.field
    }
}

#[cfg(test)]
mod tests {
    use super::*;
    
    #[test]
    fn test_scaffold_generation() {
        let generator = ScaffoldBuilder::new("Post")
            .table("posts")
            .field(
                FieldBuilder::new("title", FieldType::String)
                    .label("Title")
                    .required()
                    .searchable()
                    .build()
            )
            .field(
                FieldBuilder::new("content", FieldType::Textarea)
                    .label("Content")
                    .required()
                    .in_list(false)
                    .build()
            )
            .field(
                FieldBuilder::new("status", FieldType::Select)
                    .label("Status")
                    .options(vec![
                        ("draft".to_string(), "Draft".to_string()),
                        ("published".to_string(), "Published".to_string()),
                    ])
                    .build()
            )
            .build();
        
        let files = generator.generate_all();
        
        assert!(files.controller.contains("PostController"));
        assert!(files.controller.contains("register_routes"));
        assert!(files.list_view.contains("Posts"));
        assert!(files.form_view.contains("New Post"));
        assert!(files.show_view.contains("Post Details"));
    }
    
    #[test]
    fn test_field_builder() {
        let field = FieldBuilder::new("email", FieldType::Email)
            .label("Email Address")
            .required()
            .validation("email", "Must be a valid email")
            .build();
        
        assert_eq!(field.name, "email");
        assert_eq!(field.label, "Email Address");
        assert!(field.required);
        assert_eq!(field.validation.len(), 2);
    }
}
