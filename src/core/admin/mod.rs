use serde::{Serialize, Deserialize};

/// Field configuration for scaffolding
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ScaffoldField {
    pub name: String,
    pub field_type: FieldType,
    pub label: String,
    pub required: bool,
    pub searchable: bool,
    pub sortable: bool,
    pub in_list: bool,  // Show in list view
    pub in_form: bool,  // Show in create/edit form
    pub in_show: bool,  // Show in detail view
    pub validation: Vec<ValidationRule>,
    pub default_value: Option<serde_json::Value>,
    pub options: Option<Vec<(String, String)>>,  // For select fields
}

#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
pub enum FieldType {
    String,
    Text,
    Integer,
    Float,
    Boolean,
    Date,
    DateTime,
    Email,
    Url,
    Password,
    Select,
    Textarea,
    File,
    Image,
    Json,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ValidationRule {
    pub rule: String,  // required, min:3, max:255, email, unique, etc.
    pub message: String,
}

/// Scaffold configuration
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ScaffoldConfig {
    pub model_name: String,
    pub table_name: String,
    pub fields: Vec<ScaffoldField>,
    pub per_page: u32,
    pub searchable: bool,
    pub sortable: bool,
    pub export_csv: bool,
    pub soft_delete: bool,
}

impl Default for ScaffoldConfig {
    fn default() -> Self {
        Self {
            model_name: "Model".to_string(),
            table_name: "models".to_string(),
            fields: vec![],
            per_page: 15,
            searchable: true,
            sortable: true,
            export_csv: false,
            soft_delete: false,
        }
    }
}

