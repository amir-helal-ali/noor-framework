    /// Generate validation code
    fn generate_validation_code(&self) -> String {
        let mut validations = Vec::new();
        
        for field in &self.config.fields {
            if field.required {
                validations.push(format!(
                    r#"        Validator::required(
            body.get("{}").and_then(|v| v.as_str()).unwrap_or(""),
            "{}"
        )?;"#,
                    field.name, field.name
                ));
            }
            
            for rule in &field.validation {
                match rule.rule.as_str() {
                    "email" => {
                        validations.push(format!(
                            r#"        if let Some(email) = body.get("{}").and_then(|v| v.as_str()) {{
            if !email.is_empty() {{
                Validator::email(email, "{}")?;
            }}
        }}"#,
                            field.name, field.name
                        ));
                    }
                    "min" => {
                        if let Some(min) = rule.rule.strip_prefix("min:") {
                            validations.push(format!(
                                r#"        if let Some(val) = body.get("{}").and_then(|v| v.as_str()) {{
            if !val.is_empty() {{
                Validator::min_length(val, "{}", {})?;
            }}
        }}"#,
                                field.name, field.name, min
                            ));
                        }
                    }
                    "max" => {
                        if let Some(max) = rule.rule.strip_prefix("max:") {
                            validations.push(format!(
                                r#"        if let Some(val) = body.get("{}").and_then(|v| v.as_str()) {{
            if !val.is_empty() {{
                Validator::max_length(val, "{}", {})?;
            }}
        }}"#,
                                field.name, field.name, max
                            ));
                        }
                    }
                    _ => {}
                }
            }
        }
        
        validations.join("\n")
    }
    
